day = int(input("Digite o numero do dia da semana (1, 2, ..., 7): "))

if day == 1:
    print("Domingo")
elif day == 2:
    print("Segunda")
elif day == 3:
    print("Terca")
elif day == 4:
    print("Quarta")
elif day == 5:
    print("Quinta")
elif day == 6:
    print("Sexta")
elif day == 7:
    print("Sabado")
else:
    print("Valor invalido")
